//get all rooms endpoint
app.get("/api/v1/rooms", middleware, async (req, res) => {
    const userId = req.userId;
  
    try {
      const rooms = await prismaClient.room.findMany({
        where: {
          OR: [
            { adminId: userId },                     // rooms user created
            { members: { some: { userId } } }       // rooms user joined
          ]
        },
        orderBy: { createdAt: "desc" },
        include: {
          admin: { select: { name: true } }
        }
      });
  
      res.json({ rooms });
  
    } catch (e) {
      console.log(e);
      res.status(500).json({
        message: "Error fetching rooms"
      });
    }
  });
  


app.post("/api/v1/room/join/:inviteCode", middleware, async (req, res) => {
    const { inviteCode } = req.params;
    const userId = req.userId;

    if (!userId) {
        res.status(401).json({ message: "Unauthorized" });
        return;
    }

    try {
        const room = await prismaClient.room.findFirst({
            where: { inviteCode }
        });

        if (!room) {
            res.status(404).json({ message: "Invalid or expired invite link" });
            return;
        }

        await prismaClient.roomMember.upsert({
            where: {
                userId_roomId: {
                    userId,
                    roomId: room.id
                }
            },
            update: {},
            create: {
                userId,
                roomId: room.id
            }
        });

        res.json({
            message: "Joined room",
            roomId: room.id,
            slug: room.slug
        });
        return;

    } catch (e) {
        console.log(e);
        res.status(500).json({ message: "Error joining room" });
        return;
    }
});


app.get("/api/v1/room/:roomId/invite", middleware, async (req, res) => {
    const roomId = Number(req.params.roomId);
    const userId = req.userId;

    const room = await prismaClient.room.findFirst({
        where: {
            id: roomId,
            adminId: userId  // Only admin can get invite link
        }
    });

    if (!room) {
        res.status(403).json({ message: "Not allowed" });
        return;
    }

    res.json({
        inviteLink: `http://localhost:3000/invite/${room.inviteCode}`
    });
});



app.listen(3001);